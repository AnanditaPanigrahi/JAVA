import java.util.PriorityQueue;

public class priorityqueue {
    public static void main(String[] args) {
        PriorityQueue pq = new PriorityQueue();
        pq.add(1000);
        pq.add(20);
        pq.add(1500);
        pq.add(30);
        pq.add(0);
        System.out.println(pq);
    }
}
